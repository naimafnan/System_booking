     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $providerDetails=provider_details::where('id',auth()->user()->id)->first();
        $providerDetails->start_time=$request->Startime;
        $providerDetails->end_time=$request->EndTime;
        $providerDetails->start_rest_time=$request->startRestTime;
        $providerDetails->end_rest_time=$request->endRestTime;
        $providerDetails->slot_duration=$request->TimeSlots;
        $providerDetails->update();
        return redirect()->back()->with('msg','Your schedule has been updated');
    }

